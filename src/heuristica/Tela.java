    private boolean gerar() {
        if (jtfLinh.getText().trim().length() == 0 | jtfColun.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos");
            return false;
        }

        try {
            colun = Integer.parseInt(jtfColun.getText().trim());
            linh = Integer.parseInt(jtfLinh.getText().trim());

        } catch (Exception er) {
            JOptionPane.showMessageDialog(this, "Insira somente números inteiros!");
            return false;
        }
        if (colun <= 0 || linh <= 0 || colun > 200 || linh > 200) {
            JOptionPane.showMessageDialog(this, "Insira somente números inteiros maiores que zero e monores ou iguais a 200!");
            return false;
        }
        resolvido = false;
        if (colun > linh) {
            tamanhNo = 400 / colun;
        } else {
            tamanhNo = 400 / linh;
        }
        matriz = new No[colun][linh];
        for (int i = 0; i < colun; i++) {
            for (int j = 0; j < linh; j++) {
                matriz[i][j] = (new No(0));
                matriz[i][j].setCoordenada(new Coordenada(i, j));
            }
        }
        chegada = null;
        partida = null;
        caminho = null;
        caminhoGuloso = null;
        labelCustoA.setText("");
        labelCustoGulosa.setText("");
        jpDesenho.update(jpDesenho.getGraphics());

        return true;
    }


