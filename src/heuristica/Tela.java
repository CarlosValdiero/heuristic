    private void jpDesenhoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jpDesenhoMouseClicked
        if (matriz == null) {
            JOptionPane.showMessageDialog(this, "Insira a dimensão da matriz!");
            return;
        }
        if (resolvido) {
            return;
        }

        int x = evt.getX() / tamanhNo;
        int y = evt.getY() / tamanhNo;
        if (x >= colun || y >= linh) {
            return;
        }
        System.out.println(x);
        if (tipo == 4) {
            if (partida != null) {
                partida.setCoord(x, y);
            } else {
                partida = new Coordenada(x, y);
            }
        } else if (tipo == 5) {
            if (chegada != null) {
                chegada.setCoord(x, y);
            } else {
                chegada = new Coordenada(x, y);
            }
        } else {
            matriz[x][y].setTipo(tipo);

        }
        jpDesenho.update(jpDesenho.getGraphics());

    }//GEN-LAST:event_jpDesenhoMouseClicked

    private void btnResolvMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnResolvMouseClicked

        if (chegada == null | partida == null) {
            JOptionPane.showMessageDialog(this, "Sete a chegada e a saída!");
            return;
        }
        if (resolvido) {
            return;
        }
        criaGrafo();
        boolean g=gulosa();
        boolean a=aEstrela();
        if(a&&g);
        else if(!a&&!g)
            JOptionPane.showMessageDialog(this, "Não foi encontada nenhuma solução!");
        else if(!a)
            JOptionPane.showMessageDialog(this, "Não foi encontada nenhuma solução para Busca A*!");
        else
            JOptionPane.showMessageDialog(this, "Não foi encontada solução para Busca Gulosa!");
        jpDesenho.update(jpDesenho.getGraphics());
        
    }//GEN-LAST:event_btnResolvMouseClicked

